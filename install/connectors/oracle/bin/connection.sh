CONFIG_STRING=$1
CONFIG_SEPARATOR=','

NAME_INDEX=1
TYPE_INDEX=2
USERNAME_INDEX=3
PASSWORD_INDEX=4
SID_INDEX=5

CONNECTION_NAME=$(echo $CONFIG_STRING | cut -d "$CONFIG_SEPARATOR" -f $NAME_INDEX)
CONNECTION_TYPE=$(echo $CONFIG_STRING | cut -d "$CONFIG_SEPARATOR" -f $TYPE_INDEX)
USERNAME=$(echo $CONFIG_STRING | cut -d "$CONFIG_SEPARATOR" -f $USERNAME_INDEX)
PASSWORD=$(echo $CONFIG_STRING | cut -d "$CONFIG_SEPARATOR" -f $PASSWORD_INDEX)
SID=$(echo $CONFIG_STRING | cut -d "$CONFIG_SEPARATOR" -f $SID_INDEX)

echo $USERNAME/$PASSWORD@$SID
