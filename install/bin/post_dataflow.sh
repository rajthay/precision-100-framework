#!/bin/bash

DATAFLOW=$1

echo "START POST DATAFLOW $DATAFLOW"

sqlplus -s /nolog << EOL
CONNECT $USERNAME/$PASSWORD@$SID
SET FEEDBACK OFF
EXEC PROGRESS_LOGS_PKG.LOG('$0','POST-DATAFLOW','$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $DATAFLOW');
EXEC PERFORMANCE_LOGS_PKG.LOG('$PROJECT_FOLDER / $EXECUTION_NAME', 'DATAFLOW', '$DATAFLOW', 'END');
EXIT
EOL

echo "END POST DATAFLOW $DATAFLOW"
