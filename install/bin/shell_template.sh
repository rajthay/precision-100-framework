CONTAINER=$1
FILE_NAME=$(echo $2 | cut -d ',' -f 2)
echo "        START SHELL ADAPTOR $CONTAINER_FOLDER/$CONTAINER/$FILE_NAME"

if test "$SIMULATION_MODE" = "TRUE"; then
   sleep $SIMULATION_SLEEP;
   echo "        END SHELL ADAPTOR $CONTAINER_FOLDER/$CONTAINER/$FILE_NAME"
   exit;
fi

$PRECISION100_FOLDER/bin/audit.sh  $0 "PRE-SHELL" "$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $CONTAINER / $FILE_NAME" "$PROJECT_FOLDER / $EXECUTION_NAME" "SHELL" $0 "START"

$CONTAINER_FOLDER/$CONTAINER/$FILE_NAME 1> >(tee -a $PRECISION100_LOG_FOLDER/sh.log) 2> >(tee -a $PRECISION100_LOG_FOLDER/sh-err.log >&2)

$PRECISION100_FOLDER/bin/audit.sh  $0 "POST-SHELL" "$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $CONTAINER / $FILE_NAME" "$PROJECT_FOLDER / $EXECUTION_NAME" "SHELL" $0 "END"

echo "        END SHELL ADAPTOR $CONTAINER_FOLDER/$CONTAINER/$FILE_NAME"
