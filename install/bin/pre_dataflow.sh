#!/bin/bash

DATAFLOW=$1

echo "START PRE DATAFLOW $DATAFLOW"

sqlplus -s /nolog << EOL
CONNECT $USERNAME/$PASSWORD@$SID
SET FEEDBACK OFF
EXEC PROGRESS_LOGS_PKG.LOG('$0','PRE-DATAFLOW','$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $DATAFLOW');
EXEC PERFORMANCE_LOGS_PKG.LOG('$PROJECT_FOLDER / $EXECUTION_NAME', 'DATAFLOW', '$DATAFLOW', 'START');
EXIT
EOL

echo "END PRE DATAFLOW $DATAFLOW"
