#!/bin/bash

PIPELINE=$1

echo "  START PRE PIPELINE $PIPELINE"

sqlplus -s /nolog << EOL
CONNECT $USERNAME/$PASSWORD@$SID
SET FEEDBACK OFF
EXEC PROGRESS_LOGS_PKG.LOG('$0','PRE-PIPELINE','$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $PIPELINE');
EXEC PERFORMANCE_LOGS_PKG.LOG('$PROJECT_FOLDER / $EXECUTION_NAME', 'PIPELINE', '$PIPELINE', 'START');
EXIT
EOL

echo "  END PRE PIPELINE $PIPELINE"
