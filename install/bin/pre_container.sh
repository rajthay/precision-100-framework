#!/bin/bash

CONTAINER=$1

echo "    START PRE CONTAINER $CONTAINER"

sqlplus -s /nolog << EOL
CONNECT $USERNAME/$PASSWORD@$SID
SET FEEDBACK OFF
EXEC PROGRESS_LOGS_PKG.LOG('$0','PRE-CONTAINER','$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $CONTAINER');
EXEC PERFORMANCE_LOGS_PKG.LOG('$PROJECT_FOLDER / $EXECUTION_NAME', 'CONTAINER', '$DATAFLOW', 'START');
EXIT
EOL

echo "    END PRE CONTAINER $CONTAINER"
