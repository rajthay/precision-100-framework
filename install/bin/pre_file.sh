#!/bin/bash

FILE_NAME=$1
FILE_TYPE=$2

echo "      START PRE FILE $FILE_NAME"

sqlplus -s /nolog << EOL
CONNECT $USERNAME/$PASSWORD@$SID
SET FEEDBACK OFF
EXEC PROGRESS_LOGS_PKG.LOG('$0','PRE-FILE','$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $FILE_NAME / $FILE_TYPE ');
EXIT
EOL

echo "      END PRE FILE $FILE_NAME"
