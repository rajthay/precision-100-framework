#!/bin/bash

CONTAINER=$1

echo "    START POST CONTAINER $CONTAINER"

sqlplus -s /nolog << EOL
CONNECT $USERNAME/$PASSWORD@$SID
SET FEEDBACK OFF
EXEC PROGRESS_LOGS_PKG.LOG('$0','POST-CONTAINER','$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $CONTAINER');
EXIT
EOL

echo "    END POST CONTAINER $CONTAINER"
