#!/bin/bash

PIPELINE=$1

echo "  START POST PIPELINE $PIPELINE"

sqlplus -s /nolog << EOL
CONNECT $USERNAME/$PASSWORD@$SID
SET FEEDBACK OFF
EXEC PROGRESS_LOGS_PKG.LOG('$0','POST-PIPELINE','$PROJECT_FOLDER / $EXECUTION_NAME / $OPERATION / $SIMULATION_MODE / $PIPELINE');
EXEC PERFORMANCE_LOGS_PKG.LOG('$PROJECT_FOLDER / $EXECUTION_NAME', 'PIPELINE', '$PIPELINE', 'END');
EXIT
EOL

echo "  END POST PIPELINE $PIPELINE"
