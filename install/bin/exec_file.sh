#!/bin/bash

CONTAINER=$1
FILE_NAME=$2
FILE_TYPE=$3

if test "$FILE_TYPE" = "sql"; then
    $PRECISION100_FOLDER/bin/sql_template.sh $FILE_NAME $CONTAINER_FOLDER/$CONTAINER/$FILE_NAME
fi
   
if test "$FILE_TYPE" = "loader"; then
   LOADER_FILE_NAME=${FILE_NAME%.*}
   $PRECISION100_FOLDER/bin/loader_template.sh $CONTAINER_FOLDER/$CONTAINER/$LOADER_FILE_NAME.ctl $SQLLDR_INPUT/$LOADER_FILE_NAME.dat $SQLLDR_LOG/$LOADER_FILE_NAME.log $SQLLDR_BAD/$LOADER_FILE_NAME.bad
fi


